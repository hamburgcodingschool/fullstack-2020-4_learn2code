// Increment Operator ++

let x = 5;
x = x + 1;
console.log(x);  // 6
x++;
console.log(x);  // 7
x+=1;
console.log(x);  // 8


// Decrement Operator
x = x - 1;
console.log(x);  // 7
x--;
console.log(x);  // 6
x-=1;
console.log(x);  // 5


// It works with larger values as well
x = x - 4;
x-=4;

x = x + 4;
x+=4;
